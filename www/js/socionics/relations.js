// Возвращает название интертипного отношения type1 -> type2.
export function getRelation(type1, type2) {
  const i = TYPES.indexOf(type1);
  const j = TYPES.indexOf(type2);
  if (i === -1 || j === -1) return "деловые";

  const a = TIM[i];
  const b = TIM[j];

  // позиция базовой функции type1 в стеке type2
  let k1 = 0;
  for (let x = 0; x < 8; x++) { if (a[0] === b[x]) { k1 = x + 1; break; } }
  // позиция творческой функции type1 в стеке type2
  let k2 = 0;
  for (let x = 0; x < 8; x++) { if (a[1] === b[x]) { k2 = x + 1; break; } }

  switch (k1) {
    case 1: return (k2 === 2) ? "тождество" : "родственные";
    case 2: return (k2 === 1) ? "зеркальность" : "подревизный";
    case 3: return (k2 === 2) ? "деловые" : "суперэго";
    case 4: return (k2 === 1) ? "ревизор" : "конфликт";
    case 5: return (k2 === 6) ? "дуальность" : "полудуальность";
    case 6: return (k2 === 5) ? "активация" : "подзаказной";
    case 7: return (k2 === 6) ? "мираж" : "полная противоположность";
    case 8: return (k2 === 7) ? "квазитождество" : "заказчик";
    default: return "деловые";
  }
}

// Полная матрица 16x16 (строится один раз из алгоритма; экспортируется для tools/buildData.js).
export const RELATION_MATRIX = TYPES.map(a => TYPES.map(b => getRelation(a, b)));

